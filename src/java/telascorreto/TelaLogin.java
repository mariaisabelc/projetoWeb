    private void botaoentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoentrarActionPerformed
    Usuario usuario = new Usuario();
    usuario.setLogin(nomeLogin.getText());
    usuario.setSenha(new String(senhaUsuario.getPassword()));

    Usuario usuarioAutenticado = UsuarioBD.validarUsuarioSeguro(usuario);

    if (usuarioAutenticado != null) {
    
        TelaInicial telaInicial = new TelaInicial();
        telaInicial.setVisible(true);

        dispose();
    } else {
   
        JOptionPane.showMessageDialog(this, "Usuário ou senha incorretos", "Erro de Autenticação", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_botaoentrarActionPerformed

